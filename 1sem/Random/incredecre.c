#include<stdio.h>
void main()
{
    printf("Illustration of Pre and Post Increment\n");
    int x=5,y=10;
    printf("Value of x take is %d\n",x);
    printf("Value of x at pre-increment is %d\n",x++);
    printf("Value of x after pre-increment is %d\n",x);
    printf("Illustration of Pre and Post Decrement\n");
    printf("Value of y take is %d\n",y);
    printf("Value of y at pre-increment is %d\n",y--);
    printf("Value of y after pre-increment is %d\n",y);

}